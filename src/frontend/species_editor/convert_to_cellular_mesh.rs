// Species editor — convert an imported `.glb` into AEONS cells.
//
// This is the voxelization step: it rebuilds the imported (and
// user-scaled) mesh out of AEONS rhombic-dodecahedron cells of fixed
// size and fixed orientation, so every generated cell snaps to its
// neighbours exactly as the growth module's cells do (no gaps).
//
// ── Mathematical foundation ───────────────────────────────────────────────────
//
// A rhombic dodecahedron is the Voronoi cell of the FCC lattice, and RD
// cells tile 3-space with no gaps. So the cell CENTRES that the growth
// module places (`centre + SLOT_DIRS[i] * center_scale(EDGE_LEN)`) are
// exactly the points of an FCC lattice. That lattice is generated by the
// three face-diagonal basis vectors
//
//     b1 = a·(½,½,0)   b2 = a·(½,0,½)   b3 = a·(0,½,½)      a = 4·EDGE_LEN/√3
//
// and every one of the 18 `SLOT_DIRS` neighbour offsets is an integer
// combination of b1,b2,b3 (e.g. a·(1,0,0) = b1+b2−b3), so the set of all
// integer combinations `i·b1 + j·b2 + k·b3` is precisely the snap-grid the
// growth module uses. We anchor the lattice at the imported mesh's origin
// (the orange dot at `SPECIES_EDITOR_ORIGIN`), so site (0,0,0) sits on the
// dot — the requested seed.
//
// Fill rule (exactly as specified): a cell is placed at a lattice site iff
// the site's CENTRE lies inside the closed hull of the imported mesh.
// Insideness is a parity (even-odd) ray-cast against the mesh triangles.
// We enumerate every lattice site whose centre lies in the mesh's AABB and
// keep the ones inside the hull — equivalent to flood-filling outward from
// the centre, but robust to concavities and to the origin sitting in a
// pocket.
//
// The result becomes a single "Base Body" part with a `NoSymmetry` OCG
// (body parts / appendages / bilateral encoding are intentionally out of
// scope here). The cell type is taken from the Metabolism cycler.

use bevy::math::Mat3;
use bevy::mesh::{Indices, Mesh, PrimitiveTopology, VertexAttributeValues};
